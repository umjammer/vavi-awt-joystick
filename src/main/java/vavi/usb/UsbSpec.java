    static final int HASH1 = 0x10;
    static final int HASH2 = 0x02;
    static final int HASHSZ = 512;

    private static int hashnum(int num) {
        int mask1 = HASH1 << 27;
        int mask2 = HASH2 << 27;

        for (; mask1 >= HASH1; mask1 >>= 1, mask2 >>= 1)
            if ((num & mask1) != 0)
                num ^= mask2;
        return num & (HASHSZ - 1);
    }

